#include "libft.h"

void	ft_print_strs(char **strs)
{
	int	i;

	i = 0;
	while (strs && strs[i])
	{
		ft_putstr_fd(strs[i], 2);
		ft_putstr_fd("\n", 2);
		i++;
	}
}
